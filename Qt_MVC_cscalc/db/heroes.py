def get_hero_list(hero_class):
    ret = []
    heroes = HERO_RECOMMONDATIONS[hero_class]
    for hero in heroes:
        text = f"({hero['element']}) - {hero['original_zh']}"
        if hero['promotion_zh'] != "":
            text += f"/{hero['promotion_zh']}"
        ret.append(text)
    return ret

